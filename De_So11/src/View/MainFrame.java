    private void writeData(int i) {
        if (i == 1) {
            showData(modelPV1, listPV);
            showData(modelPV2, listPV);
            showData(modelPV3, listPV);
            f.writeFile(listPV, "PV.txt");
        }
        if (i == 2) {
            showData(modelBV1, listBV);
            showData(modelBV2, listBV);

            f.writeFile(listBV, "BV.txt");
        }
        if (i == 3) {
            showData(modelDS, listDS);
            f.writeFile(listDS, "DS.txt");
        }
    }

    private void showData(DefaultTableModel model, List list) {
        model.setRowCount(0);
        for (Object t : list) {
            if (t instanceof PhongVien) {
                model.addRow(new Object[]{
                    ((PhongVien) t).getMaPV(), ((PhongVien) t).getHoTen(), ((PhongVien) t).getDiaChi(), ((PhongVien) t).getLoaiPV()
                });
            }
            if (t instanceof KieuBaiViet) {
                model.addRow(new Object[]{
                    ((KieuBaiViet) t).getMa(), ((KieuBaiViet) t).getTenBai(), ((KieuBaiViet) t).getGia()
                });
            }
            if (t instanceof BangTinhCong) {
                model.addRow(new Object[]{
                    ((BangTinhCong) t).getPhongVien().getMaPV(), ((BangTinhCong) t).getPhongVien().getHoTen(), ((BangTinhCong) t).getKieuBaiViet().getTenBai(), ((BangTinhCong) t).getSoLuong()
                });
            }
        }
    }
