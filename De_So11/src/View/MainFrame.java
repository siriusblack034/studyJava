        Map<Integer, Integer> map = new HashMap<>();
        List<SoLuong> list = new ArrayList<>();
        for(BangTinhCong ds : listDS){
            int ma = ds.getPhongVien().getMaPV();
            int sl = ds.getSoLuong();
            map.put(ma,(map.get(ma)== null) ? sl : map.get(ma) + sl);
        }
        Set<Integer> set = new TreeSet<>(map.keySet());
        for(Integer a : set){
            list.add(new SoLuong(a, map.get(a)));
        }
        Collections.sort(list, new Comparator<SoLuong>() {
            @Override
            public int compare(SoLuong o1, SoLuong o2) {
               return o1.getSl() < o2.getSl() ?1:-1;
            }
        });
        modelDS.setRowCount(0);
        for(SoLuong s : list){
            for(BangTinhCong t : listDS){
                if(s.getMaPV() == t.getPhongVien().getMaPV()){
                    modelDS.addRow(new Object[]{
                    ((BangTinhCong) t).getPhongVien().getMaPV(), ((BangTinhCong) t).getPhongVien().getHoTen(), ((BangTinhCong) t).getKieuBaiViet().getTenBai(), ((BangTinhCong) t).getSoLuong()
                });
                }
            }
        }
