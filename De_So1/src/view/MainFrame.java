    private void showDataDs(List<DanhSachMua> listDS) {
        modelDS.setRowCount(0);
        for (DanhSachMua ds : listDS) {
            Set<MatHang> set = ds.getItems().keySet();
            for (MatHang mh : set) {
                modelDS.addRow(new Object[]{ds.getKhachHang().getHoTen(), mh.getTenHang(), ds.getItems().get(mh), tongTien(mh.getMaHang(), ds.getItems().get(mh))});
            }
        }
    }

    private double tongTien(int maHang, Integer get) {
        double gia = 0;
        for (MatHang mh : listMH) {
            if (mh.getMaHang() == maHang) {
                gia = (double) mh.getGiaBan();
            }
        }
        return gia * get;
    }

    private void showDS() {
        listDS = control.readFile("QLBH.txt");
        for (DanhSachMua ds : listDS) {
            Set<MatHang> set = ds.getItems().keySet();
            for (MatHang mh : set) {
                System.out.println(mh.getMaHang() + " " + ds.getItems().get(mh));
            }
        }
        showDataDs(listDS);

    }

