    private void bSub3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSub3ActionPerformed
        int indexKH = tbKH1.getSelectedRow();
        int indexMH = tbMH1.getSelectedRow();
        if (indexKH == -1 || indexMH == -1) {
            JOptionPane.showMessageDialog(rootPane, "Hay chon Khach hang va Mat Hang o bang tren !");
        } else {
            int sl = Integer.parseInt(tSL.getText());
            MatHang mh = listMH.get(indexMH);
            KhachHang kh = listKH.get(indexKH);

            if (listDS.size() == 0) {
                listDS.add(new DanhSachMua(mh, sl, kh));
            } else {
                int check = 1;
                for (DanhSachMua ds : listDS) {
                    if (ds.getKhachHang().getMaKH() == kh.getMaKH()) {
                        ds.setItems(mh, sl);
                        check = 0;
                    }
                }

                if (check == 1) {
                    DanhSachMua ds1 = new DanhSachMua(mh, sl, kh);
                    listDS.add(ds1);
                }
            }
            control.writeFile(listDS, "QLBH.txt");

            showDataDs(listDS);
        }


    }//GEN-LAST:event_bSub3ActionPerformed

