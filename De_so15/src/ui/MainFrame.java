    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String ten = tTenSV.getText().trim();
        String diaChi = tDiaChi.getText().trim();
        String ngaySinh = tNgaySinh.getText().trim();
        String lop = tLop.getText().trim();
        try {
            if(ten.length() ==0 || diaChi.length() ==0 || ngaySinh.length() == 0 || lop.length() == 0){
                throw new Exception("Khong dc bo trong !");
            }
            if(ten.matches("^[a-zA-Z. ]{1,}$") == false){
                throw new Exception("Ten phai toan la chu!");
            }
            System.out.println(ngaySinh.matches("^(0[1-9]|[1-2]9|[1-9]|3[0-2]).(0[1-9]|1[0-2]|[1-9]).([0-9]{4})$"));
            if(lop.matches("^(D|d)(1[0-9]|20)(CQ|cq)(CN|cn|DT|AT|PT|VT|KT|QT|MR)(0[1-9]|1[0-2])[B|N]$") == false)
                throw new Exception("lop khong dung dinh dang!");
            listSV.add(new SinhVien(idSV, ten, diaChi, ngaySinh, lop));
            writeData(1);
            idSV++;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

