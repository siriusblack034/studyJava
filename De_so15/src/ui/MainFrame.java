    private void writeData(int i) {
        if(i==1 ){
            showData(modelSV1,listSV);
            showData(modelSV1,listSV);
            showData(modelSV1,listSV);
            f.writeFile(listSV, "SV.txt");
        }
        if(i==2 ){
            showData(modelMH1,listMH);
            showData(modelMH2,listMH);
            f.writeFile(listMH, "MH.txt");
        }
        if(i==3 ){
            showData(modelBD,listBD);
            f.writeFile(listBD, "BD.txt");
        }
    }

    private void showData(DefaultTableModel model, List list) {
        model.setRowCount(0);
        for(Object t : list){
            if(t instanceof SinhVien){
                model.addRow(new Object[]{
                    ((SinhVien) t).getMa(),((SinhVien) t).getHoTen(),((SinhVien) t).getDiaChi(),((SinhVien) t).getNgaySinh(),((SinhVien) t).getLop()
                });
            }
            if(t instanceof MonHoc){
                model.addRow(new Object[]{
                    ((MonHoc) t).getMa(),((MonHoc) t).getTenMon(),((MonHoc) t).getSoDvht(),((MonHoc) t).getLoaiMon()
                });
            }
            if(t instanceof BangDiem){
                model.addRow(new Object[]{
                    ((BangDiem) t).getSinhVien().getMa(),((BangDiem) t).getSinhVien().getHoTen(),((BangDiem) t).getMonHoc().getTenMon(),((BangDiem) t).getDiem()
                });
            }
        }
    }
