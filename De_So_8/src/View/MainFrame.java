    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int index1 = jTable3.getSelectedRow();
        int index2 = jTable4.getSelectedRow();
        if (index1 == -1 || index2 == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chon data de xoa!");
            return;
        }
        NhanVien nv = listNV.get(index1);
        MatHang mh = listMH.get(index2);
        List<DanhSach> list = listDS.stream().filter(ds -> ds.getNhanVien().getMa() == nv.getMa()).collect(Collectors.toList());
        try {
            int sl = Integer.parseInt(jTextField7.getText().trim());
            if (sl > 0) {
                int check = 0;
                for (DanhSach t : list) {
                    if (t.getMatHang().getMa() == mh.getMa()) {
                        sl += t.getSoLuong();
                        t.setSoLuong(sl);
                        check = 1;
                    }
                }
                if (check == 0) {
                    listDS.add(new DanhSach(nv, mh, sl));

                }
                writeData(3);
            } else {
                JOptionPane.showMessageDialog(rootPane, "SL khong dc am!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "SL la so va khong dc bo trong!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

