    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        int index = jTable6.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chon data de xoa!");
            return;
        }
        NhanVien nv = listNV.get(index);
        List<DanhSach> list = listDS.stream().filter(ds -> ds.getNhanVien().getMa() == nv.getMa()).collect(Collectors.toList());
        modelTK.setRowCount(0);
        double tongLai = 0;
        for (DanhSach ds : list) {
            modelTK.addRow(new Object[]{
                ds.getMatHang().getMa(), ds.getMatHang().getTen(), ds.getSoLuong(), ds.getMatHang().getGiaBan() - ds.getMatHang().getGiaMua()
            });
            tongLai += (ds.getMatHang().getGiaBan() - ds.getMatHang().getGiaMua()) * ds.getSoLuong();
        }
        tenNV.setText(nv.getTen());
        tienCong.setText((tongLai * 2) / 100 + "");
    }//GEN-LAST:event_jButton9ActionPerformed

