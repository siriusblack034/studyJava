    private void showDataNV() {
        listNV = f.readFile("NV.txt");
        if (listNV.size() > 0) {
            idNV = listNV.get(listNV.size() - 1).getMa() + 1;

        } else {
            idNV = 1000;
        }
        writeData(1);
    }

    private void showDataMH() {
        listMH = f.readFile("MH.txt");
        if (listMH.size() > 0) {
            idMH = listMH.get(listMH.size() - 1).getMa() + 1;
        } else {
            idMH = 1000;
        }
        writeData(2);
    }

    private void showDataDS() {
        listDS = f.readFile("DS.txt");
        writeData(3);
    }

    private void writeData(int i) {
        if (i == 1) {
            showData(modelNV1, listNV);
            showData(modelNV2, listNV);
            showData(modelNV3, listNV);
            f.writeFile(listNV, "NV.txt");
        }
        if (i == 2) {
            showData(modelMH1, listMH);
            showData(modelMH2, listMH);

            f.writeFile(listMH, "MH.txt");
        }
        if (i == 3) {
            showData(modelDS, listDS);
            f.writeFile(listDS, "DS.txt");
        }
    }

    private void showData(DefaultTableModel model, List list) {
        model.setRowCount(0);
        for (Object t : list) {
            if (t instanceof NhanVien) {
                model.addRow(new Object[]{
                    ((NhanVien) t).getMa(), ((NhanVien) t).getTen(), ((NhanVien) t).getDiaChi(), ((NhanVien) t).getSdt()
                });
            }
            if (t instanceof MatHang) {
                model.addRow(new Object[]{
                    ((MatHang) t).getMa(), ((MatHang) t).getTen(), ((MatHang) t).getNhom(), ((MatHang) t).getGiaMua(), ((MatHang) t).getGiaBan()
                });
            }
            if (t instanceof DanhSach) {
                model.addRow(new Object[]{
                    ((DanhSach) t).getNhanVien().getMa(), ((DanhSach) t).getNhanVien().getTen(), ((DanhSach) t).getMatHang().getTen(), ((DanhSach) t).getSoLuong()
                });
            }
        }
    }
