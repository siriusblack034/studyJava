    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String ten = jTextField1.getText().trim();
            String diaChi = jTextField2.getText().trim();
            String sdt = jTextField3.getText().trim();
            int sdt1 = Integer.parseInt(sdt);
            if (ten.length() > 0 && diaChi.length() > 0) {
                if (sdt1 > 0) {
                    listNV.add(new NhanVien(idNV, ten, diaChi, sdt));
                    writeData(1);
                    idNV++;
                } else {
                    JOptionPane.showMessageDialog(rootPane, "SDT sai dinh dang");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Khong dc bo trong data!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "SDT phai la so");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            String ten = jTextField4.getText().trim();
            String nhom = jComboBox1.getSelectedItem().toString();
            double giaMua = Double.parseDouble(jTextField5.getText().trim());
            double giaBan = Double.parseDouble(jTextField6.getText().trim());
            if (ten.length() == 0 ) {
                throw new HandleException("Khong duoc de trong data!");
            }
            if (giaMua <= 0 || giaBan <= 0) {
                throw new HandleException("gia phai duong");
            }
            listMH.add(new MatHang(idMH, ten, nhom, giaMua, giaBan));
            writeData(2);
            idMH++;
        } catch (HandleException aException) {
            JOptionPane.showMessageDialog(rootPane, aException.getS());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gia phai la so!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

