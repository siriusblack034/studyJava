    private void showDataCN() {
        listCN = f.readFile("CN.txt");
        if (listCN.size() > 0) {
            idCN = listCN.get(listCN.size() - 1).getMaCN() + 1;
        } else {
            idCN = 1000;
        }
        writeData(1);
    }

    private void showDataDS() {
        listDS = f.readFile("DS.txt");
        writeData(3);

    }

    private void showDataX() {
        listX = f.readFile("XSX.txt");
        if (listX.size() > 0) {
            idX = listX.get(listX.size() - 1).getMaX() + 1;
        } else {
            idX = 100;
        }
        writeData(2);
    }

    private void writeData(int i) {
        if (i == 1) {
            showData(modelCN1, listCN);
            showData(modelCN2, listCN);
            showData(modelCN3, listCN);
            f.writeFile(listCN, "CN.txt");
        }
        if (i == 2) {
            showData(modelX1, listX);
            showData(modelX2, listX);

            f.writeFile(listX, "XSX.txt");
        }
        if (i == 3) {
            showData(modelDS, listDS);

            f.writeFile(listDS, "DS.txt");
        }
    }

    private void showData(DefaultTableModel model, List list) {
        model.setRowCount(0);
        for (Object t : list) {
            if (t instanceof CongNhan) {
                model.addRow(new Object[]{
                    ((CongNhan) t).getMaCN(), ((CongNhan) t).getHoTen(), ((CongNhan) t).getDiaChi(), ((CongNhan) t).getSDT(), ((CongNhan) t).getBacTho()
                });
            }
            if (t instanceof XuongSanXuat) {
                model.addRow(new Object[]{
                    ((XuongSanXuat) t).getMaX(), ((XuongSanXuat) t).getTenXuong(), ((XuongSanXuat) t).getMoTa(), ((XuongSanXuat) t).getHeSoCV()
                });
            }
            if (t instanceof DanhSach) {
                model.addRow(new Object[]{
                    ((DanhSach) t).getCongNhan().getMaCN(), ((DanhSach) t).getCongNhan().getHoTen(), ((DanhSach) t).getXuongSanXuat().getTenXuong(), ((DanhSach) t).getSoNgay()
                });
            }
        }
    }
