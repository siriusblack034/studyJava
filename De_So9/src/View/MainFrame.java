    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int index1 = tbCN2.getSelectedRow();
        int index2 = tbXuong2.getSelectedRow();
        if (index1 == -1 || index2 == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chon data o 2 bang tren !");
            return;
        }
        CongNhan cn = listCN.get(index1);
        XuongSanXuat x = listX.get(index2);
        List<DanhSach> list = listDS.stream().filter(ds -> ds.getCongNhan().getMaCN() == cn.getMaCN()).collect(Collectors.toList());
        try {
            int ngay = Integer.parseInt(tSoNgay.getText().trim());
            if (ngay <= 0 || ngay > 30) {
                throw new HandleException("Ngay phai duong va nho hon 30");
            }
            int soNgay = ngay;
            for (DanhSach ds : list) {
                soNgay += ds.getSoNgay();
            }
            if (soNgay > 30) {
                throw new HandleException("Tong so ngay vuot qua 30");
            }
            int check = 0;
            for (DanhSach ds : list) {
                if (ds.getXuongSanXuat().getMaX() == x.getMaX()) {
                    ngay += ds.getSoNgay();

                    ds.setSoNgay(ngay);
                    check = 1;
                }
            }
            if (check == 0) {
                listDS.add(new DanhSach(cn, x, ngay));
            }
            writeData(3);

        } catch (HandleException e) {
            JOptionPane.showMessageDialog(rootPane, e.getMess());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ngay phai la so!");
        }

    }//GEN-LAST:event_jButton5ActionPerformed

