    private void showDataKH() {
        listKH = f.readFile("KH.txt");
        if (listKH.size() > 0) {
            idKH = listKH.get(listKH.size() - 1).getMa() + 1;

        } else {
            idKH = 10000;
        }
        write(1);

    }

    private void write(int i) {
        if (i == 1) {
            showData(modelKH1, listKH);
            showData(modelKH2, listKH);
            f.writeFile(listKH, "KH.txt");
        }
        if (i == 2) {
            showData(modelMH1, listMH);
            showData(modelMH2, listMH);
            f.writeFile(listMH, "MH.txt");
        }
    }

    private void showDataDs() {
        listDS = f.readFile("DS.txt");
        showData(modelDs, listDS);
    }

    private void showDataMH() {
        listMH = f.readFile("MH.txt");
        if (listMH.size() > 0) {
            idMH = listMH.get(listMH.size() - 1).getMa() + 1;

        } else {
            idMH = 10000;
        }
        write(2);
    }
    private void showData(DefaultTableModel model, List list) {
        model.setRowCount(0);
        for (Object t : list) {
            if (t instanceof KhachHang) {
                model.addRow(new Object[]{
                    ((KhachHang) t).getMa(), ((KhachHang) t).getTen(), ((KhachHang) t).getDiaChi(), ((KhachHang) t).getNhom()
                });
            }
            if (t instanceof MatHang) {
                model.addRow(new Object[]{
                    ((MatHang) t).getMa(), ((MatHang) t).getTen(), ((MatHang) t).getGia()
                });
            }
            if (t instanceof HoaDon) {
                model.addRow(new Object[]{
                    ((HoaDon) t).getKhachHang().getTen(), ((HoaDon) t).getMatHang().getTen(), ((HoaDon) t).getSoLuong()
                });
            }
        }
    }

    private double thanhTien(Double gia, int soLuong) {
        return gia * soLuong;
    }
