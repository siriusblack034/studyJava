    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int index1 = tbKH2.getSelectedRow();
        int index2 = tbMH2.getSelectedRow();
        if (index1 == -1 || index2 == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chon thong tin o 2 bang");
            return;

        }
        KhachHang kh = listKH.get(index1);
        MatHang mh = listMH.get(index2);
        try {
            int sl = Integer.parseInt(tSoLuong.getText().trim());
            if (sl < 0) {
                JOptionPane.showMessageDialog(rootPane, "SL khong am");
            } else {
                List<HoaDon> list = listDS.stream().filter(ds -> ds.getKhachHang().getMa() == kh.getMa()).collect(Collectors.toList());
                int check = 0;
                for (HoaDon ds : list) {
                    if (ds.getMatHang().getMa() == mh.getMa()) {
                        sl += ds.getSoLuong();
                        ds.setSoLuong(sl);
                        check = 1;
                    }
                }
                if (check == 0) {
                    listDS.add(new HoaDon(kh, mh, sl));
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "SL phai la so va khong de trong!");
        }
        showData(modelDs, listDS);
        f.writeFile(listDS, "DS.txt");
    }//GEN-LAST:event_jButton5ActionPerformed

