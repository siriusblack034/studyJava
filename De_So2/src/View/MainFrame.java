    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String ten = tTenPb.getText().trim();
            String moTa = tMoTa.getText().trim();
            int heSo = Integer.parseInt(cbHeSo.getSelectedItem().toString());
            if (ten.length() != 0 && moTa.length() != 0 && idPb <= 999) {
                listPb.add(new PhongBan(idPb, heSo, ten, moTa));
                f.writeFile(listPb, "PB.txt");
                showData(modelPb1, listPb);
                showData(modelPb2, listPb);
                idPb++;
            } else {
                JOptionPane.showMessageDialog(rootPane, "Khong duoc de trong");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton4ActionPerformed

