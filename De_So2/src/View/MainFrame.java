    private void showData(DefaultTableModel model, List list) {
        model.setRowCount(0);
        for (Object t : list) {
            if (t instanceof NhanVien) {
                model.addRow(new Object[]{
                    ((NhanVien) t).getMa(), ((NhanVien) t).getHoTen(), ((NhanVien) t).getDiaChi(), ((NhanVien) t).getSdt(), ((NhanVien) t).getBacLuong()
                });
            }
            if (t instanceof PhongBan) {
                model.addRow(new Object[]{
                    ((PhongBan) t).getMaPhong(), ((PhongBan) t).getTenPhong(), ((PhongBan) t).getMoTa(), ((PhongBan) t).getHeSo()
                });
            }
            if (t instanceof DanhSachCong) {
                Set<NhanVien> set = ((DanhSachCong) t).getItems().keySet();
                for (NhanVien nv : set) {
                    model.addRow(new Object[]{
                        nv.getHoTen(), ((DanhSachCong) t).getPhongBan().getTenPhong(), ((DanhSachCong) t).getItems().get(nv)
                    });
                }
            }
        }
    }
