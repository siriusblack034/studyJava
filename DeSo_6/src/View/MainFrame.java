    private void showDataDV() {
        listDV = f.readFile("DV.txt");
        if (listDV.size() > 0) {
            idDV = listDV.get(listDV.size() - 1).getMa() + 1;

        } else {
            idDV = 1000;
        }
        writeData(2);
    }

    private void showDataHD() {
        listHD = f.readFile("HD.txt");
        showData(modelHD, listHD);
    }

    private void showDataKH() {
        listKH = f.readFile("KH.txt");
        if (listKH.size() > 0) {
            idKH = listKH.get(listKH.size() - 1).getMa() + 1;

        } else {
            idKH = 10000;
        }
        writeData(1);
    }

    private void writeData(int i) {
        if (i == 1) {
            showData(modelKH1, listKH);
            showData(modelKH2, listKH);
            showData(modelKH3, listKH);
            f.writeFile(listKH, "KH.txt");
        }
        if (i == 2) {
            showData(modelDV1, listDV);
            showData(modelDV2, listDV);
            f.writeFile(listDV, "DV.txt");
        }
    }

    private void showData(DefaultTableModel model, List list) {
        model.setRowCount(0);
        for (Object t : list) {
            if (t instanceof KhachHang) {
                model.addRow(new Object[]{
                    ((KhachHang) t).getMa(), ((KhachHang) t).getHoTen(), ((KhachHang) t).getDiaChi(), ((KhachHang) t).getLoai()
                });
            }
            if (t instanceof DichVu) {
                model.addRow(new Object[]{
                    ((DichVu) t).getMa(), ((DichVu) t).getTen(), ((DichVu) t).getGia(), ((DichVu) t).getDonVi()
                });
            }
            if (t instanceof HoaDon) {
                model.addRow(new Object[]{
                    ((HoaDon) t).getKhachHang().getHoTen(), ((HoaDon) t).getDichVu().getTen(), ((HoaDon) t).getSoLuong()
                });
            }
        }
    }

