    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int index1 = jTable3.getSelectedRow();
        int index2 = jTable4.getSelectedRow();
        if (index1 == -1 || index2 == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chon data de xoa");
            return;
        }
        KhachHang kh = listKH.get(index1);
        DichVu dv = listDV.get(index2);
        List<HoaDon> list = listHD.stream().filter(hd -> hd.getKhachHang().getMa() == kh.getMa()).collect(Collectors.toList());

        try {
            if (list.size() >= 5) {
                int soLuong = Integer.parseInt(jTextField6.getText().trim());
                if (soLuong > 0) {
                    int check = 0;
                    for (HoaDon hd : list) {
                        if (hd.getDichVu().getMa() == dv.getMa()) {
                            soLuong += hd.getSoLuong();
                            hd.setSoLuong(soLuong);
                            check = 1;
                        }
                    }
                    if (check == 0 && list.size() <5) {
                        listHD.add(new HoaDon(kh, dv, soLuong));
                    }
                    if( list.size() == 5){
                        JOptionPane.showMessageDialog(rootPane, "Chi dc 5 DV");
                    }
                    showData(modelHD, listHD);
                    f.writeFile(listHD, "HD.txt");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "SL phai la so duong");
                }
            }
            else{
                JOptionPane.showMessageDialog(rootPane, "Chi dc 5 DV");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "SL phai la so va khong dc bo trong");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

